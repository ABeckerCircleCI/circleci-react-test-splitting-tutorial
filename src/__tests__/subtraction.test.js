import { subtract } from '../math';

describe('Subtraction testing', () => {
      test('4 - 1 = 3', () => {
        expect(subtract(4, 1)).toBe(3);
      });

      test('4 - 1 = 3', () => {
        expect(subtract(4, 1)).toBe(3);
      });

      test('4 - 1 = 3', () => {
        expect(subtract(4, 1)).toBe(3);
      });
})